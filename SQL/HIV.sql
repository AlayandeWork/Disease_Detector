CREATE TABLE HIV (
	id INT auto_increment,
	Gender varchar(255),
	Chronic_Diarrhea varchar(255),
	Skin_Irritation varchar(255), 
	Genital_Ulcer varchar(255), 
	Persistent_Fatigue varchar(255),
	Severe_Headache varchar(255), 
	Weight_Loss varchar(255),
	Vomiting varchar(255), 
	Severity varchar(255),
    PRIMARY KEY (id)
);