CREATE TABLE cholera (
	id INT auto_increment,
	Gender varchar(255),
	Diarrhea varchar(255),
	Dehydration varchar(255), 
	Abdominal_Cramps varchar(255), 
	Irritability varchar(255),
	Restlessness varchar(255), 
	Lethargy varchar(255),
	Vomiting varchar(255), 
	Severity varchar(255),
    PRIMARY KEY (id)
);