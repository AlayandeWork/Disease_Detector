CREATE TABLE parkinson (
	id INT auto_increment,
	Gender varchar(255),
	Akinesia varchar(255),
	Tremors varchar(255), 
	Postural_Instability varchar(255), 
	Rigidity varchar(255),
	Cognitive_Challenge varchar(255), 
	Insomnia varchar(255),
	Constipation varchar(255), 
	Severity varchar(255),
    PRIMARY KEY (id)
);